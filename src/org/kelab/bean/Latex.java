package org.kelab.bean;

public class Latex {
	String imgHtml;
	String base64Code;
	String latexCode;
	public String getImgHtml() {
		return imgHtml;
	}
	public void setImgHtml(String imgHtml) {
		this.imgHtml = imgHtml;
	}
	public String getBase64Code() {
		return base64Code;
	}
	public void setBase64Code(String base64Code) {
		this.base64Code = base64Code;
	}
	public String getLatexCode() {
		return latexCode;
	}
	public void setLatexCode(String latexCode) {
		this.latexCode = latexCode;
	}

}
